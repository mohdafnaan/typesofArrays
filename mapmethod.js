// map() : transforms each element and returns a new array.

let num =[1,2,3,4,5,6,7];

console.log(num.map((x) => x*2));

// console.log(num);

num.forEach((x)=>{
    console.log(x*2);
})

// for each and map are both same but in forEach method we get only values and we cannot perform any furthur operations ....values cannot return.

